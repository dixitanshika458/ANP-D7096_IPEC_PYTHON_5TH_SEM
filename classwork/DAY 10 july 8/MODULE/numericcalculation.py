#creating module for calculation
#creating function for addition
def calculate_addition(num1, num2):
    return num1 + num2
#creating function for subtraction
def calculate_subtraction(num1, num2):
    return num1 - num2
#creating function for multiplication
def calculate_multiplication(num1, num2):
    return num1 * num2
#creating function for division
def calculate_division(num1, num2):
    return num1 / num2
#creating function for remainder
def calculate_remainder(num1, num2):
    return num1 % num2
